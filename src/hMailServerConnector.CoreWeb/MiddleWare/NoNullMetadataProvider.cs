using Microsoft.AspNetCore.Mvc.ModelBinding.Metadata;

namespace hMailServerConnector.CoreWeb.MiddleWare
{
    public class NoNullMetadataProvider : IMetadataDetailsProvider, IDisplayMetadataProvider
    {
        public void CreateDisplayMetadata(DisplayMetadataProviderContext context)
        {
            if (context.Key.MetadataKind == ModelMetadataKind.Parameter || context.Key.MetadataKind == ModelMetadataKind.Property)
            {
                context.DisplayMetadata.ConvertEmptyStringToNull = false;
            }
        }
    }
}
